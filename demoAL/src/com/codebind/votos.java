package com.codebind;

public class votos {
    String name;
    int votos;
    int ultvoto;
    String dia;
    String hora;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVotos() {
        return votos;
    }

    public void setVotos(int votos) {
        this.votos = votos;
    }

    public int getUltvoto() {
        return ultvoto;
    }

    public void setUltvoto(int ultvoto) {
        this.ultvoto = ultvoto;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

}
